            <strong><h1>Computer Engineering </h1></strong>
            <p>Computer Engineering uses principles from Computer Science and Electrical Engineering to create hardware (physical components) and firmware which are used in a wide range of areas: consumer electronics, medical devices, communication systems, aircraft, self-driving cars, etc.
                Computer Engineering students learn how to develop, prototype, and test microchips, circuits, processors, conductors and any other component used in computer devices or systems (e.g. supercomputers, smartphones, laptops, servers, IoT gadgets). They also develop firmware, an essential type of software that allows operating systems and applications to take full advantage of the hardware.
            </p>
            <p><b>What does an Computer Engineer (COMP) Engineer do?</b><br>
                
                <b>. </b> Computer engineers create and test hardware such as motherboards, routers, circuits, and other equipment. While a programmer focuses on the software side of computer systems, computer engineers specialize in the physical components of a computer. Computer engineers work in tandem with software developers to make sure any updates function as expected.<br>
                <b>. </b>  Computer engineers often research new technology and methods in the field to create innovative products and solutions. This role can involve heavy amounts of testing and experimenting with new designs during the development process. After creating a successful design, computer engineers may supervise the manufacturing process.<br>
                <b>. </b>  Companies rely on computer engineers to create modern processors and networking hardware. Any tech companies that produce hardware need qualified computer engineers to make cutting-edge products and stay ahead of the competition.<br>
